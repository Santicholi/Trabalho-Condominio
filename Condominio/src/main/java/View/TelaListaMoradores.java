    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
    int linhaSelecionada = tabelaMoradores.getSelectedRow();
    
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um morador para alterar");
        return;
    }

    // Obtém os dados da linha selecionada
    int id = (int) tabelaMoradores.getValueAt(linhaSelecionada, 0);
    String nome = (String) tabelaMoradores.getValueAt(linhaSelecionada, 1);
    String cpf = (String) tabelaMoradores.getValueAt(linhaSelecionada, 2);
    String rg = (String) tabelaMoradores.getValueAt(linhaSelecionada, 3);
    String idade = tabelaMoradores.getValueAt(linhaSelecionada, 4).toString();
    String moraEm = tabelaMoradores.getValueAt(linhaSelecionada, 5).toString();

    // Abre a tela de cadastro em modo edição
    TelaCadastroMorador telaEdicao = new TelaCadastroMorador();
    telaEdicao.carregarDadosParaEdicao(id, nome, cpf, rg, idade, moraEm);
    telaEdicao.setVisible(true);
    
    telaEdicao.addWindowListener(new java.awt.event.WindowAdapter() {
        @Override
        public void windowClosed(java.awt.event.WindowEvent e) {
            carregarTabelaMoradores(); // Atualiza a tabela após edição
        }
    });
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        
    String id = campo_codigoMorador.getText().trim();
    String nome = campo_nomeMorador.getText().trim();

    ArrayList<Morador> resultado = controller.buscarMoradores(id, nome);
    DefaultTableModel model = (DefaultTableModel) tabelaMoradores.getModel();
    model.setRowCount(0); // Limpa a tabela

    for (Morador m : resultado) {
        model.addRow(new Object[]{
            m.getCodigo_morador(),
            m.getNome(),
            m.getCpf(),
            m.getRg(),
            m.getIdade(),
            m.getMoraEm()
        });
    }    
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
    int linhaSelecionada = tabelaMoradores.getSelectedRow();
    
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um morador para excluir.");
        return;
    }

    int id = (int) tabelaMoradores.getValueAt(linhaSelecionada, 0);
    int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este morador?", "Confirmação", JOptionPane.YES_NO_OPTION);

    if (confirmacao == JOptionPane.YES_OPTION) {
        if (controller == null) {
            controller = new MoradorController();
        }

        boolean sucesso = controller.excluirMorador(id);
        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Morador excluído com sucesso.");
            btn_buscarActionPerformed(null); // Atualiza a tabela
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir morador.");
        }
    }
    }//GEN-LAST:event_btn_excluirActionPerformed

    
