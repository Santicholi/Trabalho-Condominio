    private void carregarTabelaMoradores() {
    DefaultTableModel model = (DefaultTableModel) tabelaMoradores.getModel();
    model.setRowCount(0); // Limpa a tabela antes de preencher

    if (controller == null) {
        controller = new MoradorController();
    }

    ArrayList<Morador> lista = controller.listarTodosMoradores();

    for (Morador m : lista) {
        model.addRow(new Object[] {
            m.getCodigo_morador(),
            m.getNome(),
            m.getCpf(),
            m.getRg(),
            m.getIdade(),
            m.getMoraEm()
        });
    }
}


