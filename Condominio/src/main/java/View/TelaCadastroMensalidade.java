    public void carregarDadosParaEdicao(Mensalidade m) {
        campo_codigo.setText(String.valueOf(m.getCodigo_mensalidade()));
        campo_numeroCasa.setText(String.valueOf(m.getResidencia()));
        campo_referente.setText(m.getMesReferencia());
        campo_valor.setText(String.valueOf(m.getValor()));
    
    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
    if (m.getDataVencimento() != null) {
        campo_dtVencimento.setText(sdf.format(m.getDataVencimento()));
    }
    if (m.getDataPagamento() != null) {
        campo_dtPagamento.setText(sdf.format(m.getDataPagamento()));
    }
    
    // Configura o título da janela para indicar que é uma edição
    this.setTitle("Editar Mensalidade - Código: " + m.getCodigo_mensalidade());
    }

    private void obterProximoId() {
        try {
            int proximoId = controller.obterProximoId();
            campo_codigo.setText(String.valueOf(proximoId));
            campo_codigo.setEnabled(false);
            campo_codigo.setFocusable(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar próximo ID: " + e.getMessage());
        }
   
    } 

