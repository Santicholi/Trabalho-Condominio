    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
    
        try {
        int codigoMensalidade = 0; // ou pegar de um campo oculto se for editar
        if (!campo_codigo.getText().isEmpty()) {
            codigoMensalidade = Integer.parseInt(campo_codigo.getText());
        }

        int numeroCasa = Integer.parseInt(campo_numeroCasa.getText());
        String mesReferencia = campo_referente.getText();
        double valor = Double.parseDouble(campo_valor.getText());
        String vencimentoStr = campo_dtVencimento.getText().trim();
        LocalDate vencimentoLocalDate = LocalDate.parse(vencimentoStr, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        java.sql.Date vencimentoSql = java.sql.Date.valueOf(vencimentoLocalDate);

        String pagamentoStr = campo_dtPagamento.getText().trim();
        java.sql.Date pagamentoSql = null;
        if (!pagamentoStr.isEmpty()) {
            LocalDate pagamentoLocalDate = LocalDate.parse(pagamentoStr, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            pagamentoSql = java.sql.Date.valueOf(pagamentoLocalDate);
        }

        Mensalidade m = new Mensalidade();
        m.setCodigo_mensalidade(codigoMensalidade);
        m.setResidencia(numeroCasa);
        m.setMesReferencia(mesReferencia);
        m.setValor(valor);
        m.setDataVencimento(vencimentoSql);
        m.setDataPagamento(pagamentoSql);

        boolean sucesso;
        if (codigoMensalidade > 0 && controller.MensalidadeExiste(codigoMensalidade)) {
            controller.atualizar(m);
            sucesso = true;
        } else {
            controller.salvar(m);
            sucesso = true;
        }

        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Operação realizada com sucesso!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar mensalidade.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
        

    }//GEN-LAST:event_btn_SalvarActionPerformed

