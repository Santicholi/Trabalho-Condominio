    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
    
    String numero = campo_numero.getText();
    String rua = campo_rua.getText();
    String cep = campo_cep.getText();
    String proprietario = campo_proprietario.getText();

        try {
            boolean sucesso;
            int numeroCasa = Integer.parseInt(numero);

            if (controller.ResidenciaExiste(numeroCasa)) {
                sucesso = controller.atualizarResidencia(numero, rua, cep, proprietario);
            } else {
                sucesso = controller.salvarResidencia(numero, rua, cep, proprietario);
            }

            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Operação realizada com sucesso!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar residência.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_SalvarActionPerformed

