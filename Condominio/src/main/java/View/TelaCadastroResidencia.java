        private boolean modoEdicao = false;
        
        public void carregarDadosParaEdicao(int numero, String rua, String cep, int proprietario) {
        this.modoEdicao = true; 
        
        
        campo_numero.setEnabled(true);
        
        campo_proprietario.setText(proprietario == 0 ? "" : String.valueOf(proprietario));
        
        // Preenche os campos com os dados do morador
        campo_numero.setText(String.valueOf(numero));
        campo_rua.setText(rua);
        campo_cep.setText(cep);
        campo_proprietario.setText(String.valueOf(proprietario));

        
        // Bloqueia a edição do ID
        campo_numero.setEnabled(false);
    }
    
    private void obterProximoId(){
        try {
            int proximoId = controller.obterProximoId(); // já tem o controller
            campo_numero.setText(String.valueOf(proximoId));
            campo_numero.setEnabled(false);// impede edição de texto
            campo_numero.setFocusable(false); // evita o cursor aparecer no campo
        } catch(Exception e){
            JOptionPane.showMessageDialog(this,"Erro ao carregar próximo ID: " + e.getMessage());
        }
    } 

