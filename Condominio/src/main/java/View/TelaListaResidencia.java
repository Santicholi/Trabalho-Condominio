    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        String numero = campo_numeroCasa.getText().trim();
        String rua = campo_rua.getText().trim();

        ArrayList<Residencia> resultado = controller.buscarResidencias(numero, rua);
        DefaultTableModel model = (DefaultTableModel) tabelaResidencias.getModel();
        model.setRowCount(0); // Limpa a tabela

        for (Residencia r : resultado) {
            model.addRow(new Object[]{
            r.getNumeroCasa(),
            r.getRua(),
            r.getCep(),
            r.getProprietario(),
            });
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

