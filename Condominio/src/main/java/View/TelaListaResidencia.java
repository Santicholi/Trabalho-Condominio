    private void carregarTabelaResidencias() {
    DefaultTableModel model = (DefaultTableModel) tabelaResidencias.getModel();
    model.setRowCount(0); // Limpa a tabela antes de preencher

    if (controller == null) {
        controller = new ResidenciaController();
    }

    ArrayList<Residencia> lista = controller.listarTodasResidencias();

    for (Residencia r : lista) {
        model.addRow(new Object[] {
            r.getNumeroCasa(),
            r.getRua(),
            r.getCep(),
            r.getProprietario()
        });
    }
}
