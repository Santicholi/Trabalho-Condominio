    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        int linhaSelecionada = tabelaResidencias.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma residencia para alterar");
            return;
        }

        // Obtém os dados da linha selecionada
        int numeroCasa = (int) tabelaResidencias.getValueAt(linhaSelecionada, 0);
        String rua = (String) tabelaResidencias.getValueAt(linhaSelecionada, 1);
        String cep = (String) tabelaResidencias.getValueAt(linhaSelecionada, 2);
        int proprietario = (int) tabelaResidencias.getValueAt(linhaSelecionada, 3);


        // Abre a tela de cadastro em modo edição
        TelaCadastroResidencia telaEdicao = new TelaCadastroResidencia();
        telaEdicao.carregarDadosParaEdicao(numeroCasa,rua,cep,proprietario);
        telaEdicao.setVisible(true);

        telaEdicao.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                carregarTabelaResidencias(); // Atualiza a tabela após edição
            }
        });
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int linhaSelecionada = tabelaResidencias.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma residencia para excluir.");
            return;
        }

        int numero = (int) tabelaResidencias.getValueAt(linhaSelecionada, 0);
        int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir esta residencia?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (confirmacao == JOptionPane.YES_OPTION) {
            if (controller == null) {
                controller = new ResidenciaController();
            }

            boolean sucesso = controller.excluirResidencia(numero);
            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Residencia excluído com sucesso.");
                btn_buscarActionPerformed(null); // Atualiza a tabela
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir morador.");
            }
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

